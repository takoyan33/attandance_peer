import Head from "next/head";
import {
  getAuth,
  signInWithEmailAndPassword,
  signInWithPopup,
} from "firebase/auth";
import Image from "next/image";
import { Inter } from "@next/font/google";
import { Input } from "@mantine/core";
import { useState, useEffect } from "react";
import { database } from "../../firebaseConfig";
import { collection, addDoc, onSnapshot, getDocs } from "firebase/firestore";
import { useRouter } from "next/router";
import { query, orderBy } from "firebase/firestore";
import { useForm } from "react-hook-form";
import { InputBase, Button } from "@mantine/core";
import { MuiNavbar } from "../../components/MuiNavbar";

export default function Home() {
  const { register, handleSubmit } = useForm();
  const router = useRouter();
  const auth = getAuth();

  const SignIn = (data: any) => {
    console.log(data.email);
    alert("ログインしました");
    console.log(data.password);
    signInWithEmailAndPassword(auth, data.email, data.password)
      .then((userCredential: any) => {
        const user = userCredential.user;
        localStorage.setItem("Token", user.accessToken);
        router.push("/");
      })
      .catch((err) => {
        alert("ログインできません");
        console.log(err);
      });
  };

  return (
    <>
      <Head>
        <title>出席管理</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <MuiNavbar />

      <div className="max-w-4xl m-auto mt-10">
        <h2 className="text-center">管理者ログイン</h2>

        <div>
          <form onSubmit={handleSubmit(SignIn)}>
            <div>
              <label htmlFor="email">メールアドレス</label>
              <Input type="email" id="email" {...register("email")} />
            </div>

            <label htmlFor="password">パスワード</label>
            <Input type="password" id="password" {...register("password")} />
            <div className="my-4 m-auto text-center">
              <Button type="submit" variant="outline" color="cyan">
                ログインする
              </Button>
            </div>
          </form>
        </div>
      </div>
    </>
  );
}
